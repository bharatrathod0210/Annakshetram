// Get cities for a state
export const getCitiesForState = (state) => {
  return INDIAN_STATES_CITIES[state] || [];
};

// Validate if city belongs to state
export const validateCityInState = (city, state) => {
  if (!city || !state) return false;
  
  const cities = getCitiesForState(state);
  if (cities.length === 0) return true; // Allow if no cities defined
  
  // Case-insensitive search
  const cityLower = city.toLowerCase().trim();
  return cities.some(c => c.toLowerCase() === cityLower);
};

// Validate Indian pincode format
export const validatePincode = (pincode) => {
  if (!pincode) return false;
  
  // Indian pincode: 6 digits, first digit cannot be 0
  const pincodeRegex = /^[1-9][0-9]{5}$/;
  return pincodeRegex.test(pincode);
};

// Get state from pincode (first digit mapping)
export const getStateFromPincode = (pincode) => {
  if (!validatePincode(pincode)) return null;
  
  const firstDigit = pincode.charAt(0);
  const stateMapping = {
    '1': ['Delhi', 'Haryana', 'Punjab', 'Himachal Pradesh', 'Jammu & Kashmir', 'Chandigarh'],
    '2': ['Uttar Pradesh', 'Uttarakhand'],
    '3': ['Rajasthan', 'Gujarat'],
    '4': ['Maharashtra', 'Goa'],
    '5': ['Andhra Pradesh', 'Telangana', 'Karnataka'],
    '6': ['Tamil Nadu', 'Kerala', 'Puducherry'],
    '7': ['West Bengal', 'Odisha', 'Assam', 'Arunachal Pradesh', 'Nagaland', 'Manipur', 'Mizoram', 'Tripura', 'Meghalaya'],
    '8': ['Bihar', 'Jharkhand'],
    '9': ['Madhya Pradesh', 'Chhattisgarh'],
  };
  
  return stateMapping[firstDigit] || [];
};

// Validate if pincode matches state
export const validatePincodeForState = (pincode, state) => {
  if (!validatePincode(pincode) || !state) return false;
  
  const possibleStates = getStateFromPincode(pincode);
  if (possibleStates.length === 0) return true; // Allow if mapping not found
  
  return possibleStates.includes(state);
};
